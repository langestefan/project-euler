)

function solve_problem(w::Int64=13)
    best = 0
    i = 1
    it_count = 0

    # find the largest product of w adjacent digits
    while i <= length(digits(S)) - w + 1
        it_count += 1
        chars = digits(S)[i:(i + w - 1)]

        # if any of the digits is 0, jump to the index adjacent to the last 0
        if minimum(chars) == 0
            i += argmin(chars)
            continue
        end

        # compute the product of the digits
        product = prod(chars)
        if product > best
            best = product
        end
        i += 1
    end
    println("Iterations: $it_count")
    return best
end


